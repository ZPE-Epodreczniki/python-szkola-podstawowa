liczba = 25
print("Sprawdzamy warunki dla liczby:", liczba)

if liczba % 2 == 0:
    print("Liczba jest parzysta.")
elif liczba % 2 == 1:
    print("Liczba jest nieparzysta.")
elif liczba > 10:
    print("Liczba jest większa niż 10.")
elif liczba > 20:
    print("Liczba jest większa niż 20.")
elif liczba > 30:
    print("Liczba jest większa niż 30.")
elif liczba > 0:
    print("Liczba jest dodatnia.")
elif liczba < 0:
    print("Liczba jest ujemna.")
elif liczba > 1:
    print("Liczba jest większa niż 1.")